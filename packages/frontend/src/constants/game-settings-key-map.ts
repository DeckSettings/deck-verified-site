export const gameSettingsKeyMap: Record<string, string[]> = {
  'VSYNC': [
    'VSYNC',
    'V-SYNC',
  ],
  'FULLSCREEN': [
    'FULLSCREEN',
    'FULLSCREEN MODE',
    'FULL SCREEN',
    'FULL SCREEN MODE',
  ],
  'FRAMERATE CAP': [
    'FRAMERATE CAP',
    'FRAME RATE CAP',
  ],
}

export function standardizeGameSettingsKey(key: string): string {
  const upperKey = key.toUpperCase()
  for (const standardKey in gameSettingsKeyMap) {
    if (gameSettingsKeyMap[standardKey]?.includes(upperKey)) {
      return standardKey
    }
  }
  return upperKey
}
